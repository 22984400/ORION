export function t(path: string): string {
  return path.split('.').reduce((obj: any, key) => obj?.[key], translations) ?? path;
}

export function translateStatus(value: string): string {
  return (translations.status as Record<string, string>)[value] ?? value.replace(/_/g, ' ');
}

export function translateDay(day: string): string {
  return (translations.days as Record<string, string>)[day] ?? day;
}
